public class FloatCharDoubleShort {
    public static void main(String[] args) {
        //Integers
        int myAge = 45;

        //Byte - smaill type
        byte myByte = 127;
        System.out.println("My byte is: " + myByte);

        //Short - 2x byte
        short myShort = 128;
        System.out.println("My Short is: " + myShort);

        //Float
        float myFloat = 3.14f;
        System.out.println("My Float is: " + myFloat);

        //Double
        double myDouble = 23.89;
        System.out.println("My Double is: " + myDouble);

        //Char == character
        char myChar = 'E';
        System.out.println("My Char is: " + myChar);

        //Boolean = true or false, 1 or 0
        boolean myBoolean1 = true;
        // boolean myBoolean2 = 1; error

        // System.out.println("My Boolean is: " + myBoolean1 + "\t" + myBoolean2); error myBoolean2
        System.out.println("My Boolean is: " + myBoolean1);

    }


}
